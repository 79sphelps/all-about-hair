const mongoose = require("mongoose");
const RequestSchema = mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  message: {
    type: String,
    required: true
  },
  category: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    required: false
  }
});

const Request = mongoose.model("Request", RequestSchema);
module.exports = Request;
